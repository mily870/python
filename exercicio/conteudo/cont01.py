n=input('digite algo')
print(n.isnumeric()) #para testar número inteiro
print(n.isalpha()) #para testar letra
print(n.isalnum()) #testa se é letra ou número
print(n.isupper()) #verificar se tem letra maiuscula
print(n.isdecimal()) #verificar se tem numero decimal
print(n.islower()) #verificar letras minusculas
print(n.isspace()) #verificar se tem espaço